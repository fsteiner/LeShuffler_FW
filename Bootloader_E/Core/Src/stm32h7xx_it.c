/**
  * @brief This function handles Hard fault interrupt.
  */
void HardFault_Handler(void)
{
  /* USER CODE BEGIN HardFault_IRQn 0 */
  // Debug: Continuous fast beeping to indicate hard fault
  // Enable GPIOA clock (in case it's not enabled)
  RCC->AHB4ENR |= RCC_AHB4ENR_GPIOAEN;
  // Configure PA2 as output (buzzer)
  GPIOA->MODER = (GPIOA->MODER & ~(3UL << 4)) | (1UL << 4);
  /* USER CODE END HardFault_IRQn 0 */
  while (1)
  {
    /* USER CODE BEGIN W1_HardFault_IRQn 0 */
    // Fast beeping pattern: hard fault indicator
    GPIOA->BSRR = (1UL << 2);  // PA2 high
    for (volatile uint32_t i = 0; i < 100000; i++);
    GPIOA->BSRR = (1UL << 18); // PA2 low (reset bit)
    for (volatile uint32_t i = 0; i < 100000; i++);
    /* USER CODE END W1_HardFault_IRQn 0 */
  }
}

